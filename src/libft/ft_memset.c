#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t				i;
	unsigned char		*o;

	i = 0;
	o = s;
	while (i < n)
	{
		o[i] = c;
		i++;
	}
	s = (void *) o;
	return (s);
}
