#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*copy;

	i = 0;
	copy = s;
	while (i < n)
	{
		copy[i] = '\0';
		i++;
	}
	s = copy;
}
